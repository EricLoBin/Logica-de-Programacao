'''
Exercício-2:

- Atribua o valor 10 à variável “x”
- Atribua o valor 20 à variável “y”
- Atribua o valor 30 à variável “z”
- Verifique se “x” multiplicado por 3 é igual a “z”
- Verifique se “z” é maior que a soma de “x” e “y”
- Verifique se “z” subtraído de “y” é igual a “x”

'''

x = 10
y = 20
z = 30
if (x*3 == z):
    print('"x" multiplicado por 3 é igual a "z"')

if (z > x+y):
    print('"z" é maior que a soma de "x" e "y"')

if (z - y == x):
    print('"z" subtraído de "y" é igual a "x"')
